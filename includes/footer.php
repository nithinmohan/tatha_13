<h2><u>Registration (Workshops and Events)</u></h2><br/>
					1. Participants should have a CONFIRMED Tathva ID to participate in any general event or workshop. Envision events(Jigyasa, Srishti)and Young Engineer DO NOT require paid registration.<br/><br/>

2. All participants who have registered online for Tathva SHOULD CONFIRM their registration at the registration desk by paying registration fees and obtaining a participant badge. Participants who have not registered online also can obtain their id by reporting at the registration desk. Participants must have a CONFIRMED Tathva ID to participate in any event.<br/><br/>

3. Registration for ALL Gaming Events will be done separately at the blitzkrieg arena.
For team gaming events, at least one among the team members should have a CONFIRMED Tathva ID. All gaming participants must have a photo ID with them.<br/><br/>

4. The registration for Quizzing events will be held separately. Registration fee will be Rs.50per person per event. Those who already have a Tathva ID NEED NOT pay the above mentioned fee.<br/><br/>

5. Participants for various workshops should report at the registration desk before the commencement of the respective workshops and confirm their registration. Workshop participants CANNOT participate in general events. However, they can participate in quizzing and gaming events by following rules 3 and 4.<br/><br/>
6. Participants should produce their Tathva ID for inspection when asked for by organizers.<br/><br/>
<h2><u>Evaluation and Result Declaration Policy</u></h2><br/><br/>
1. Every participant should abide by the rules, regulations and specifications given in Tathva website for each event . A participant will be DISQUALIFIED if their equipment is not compliant with the regulations of the event or if his/her conduct is deemed unreasonable by the event co-ordinators.<br/><br/>
2. If the performance of an arbitrarily large number of participants is substandard, the final prize money distribution will be decided by the respective event managers.<br/><br/>
3. If a participant wins the event but fails in any round of multiple segment events (i.e events with multiple subsections), the prize money awarded will only be a fraction of the total prize money depending on how far the participant has progressed.<br/><br/>
4. In case of two first/second prizes (A tie which cannot be settled by the tie breaking rounds or any method descried in the specific event rules) then the prize money will be decided as follows: First/second positions(2 nos) : (First/second prize money + Second/third Prize money)/2, Second position: Third Prize money. Third place will not be declared in this case.<br/>
In case, 2 third positions arise, the prize money will be split equally between participants.<br/><br/>
5. If the participation for any event is low, the model given below will be used as a reference for awarding the prize money<br/>
&nbsp;&nbsp;&nbsp;&nbsp;a) Only one team - Consolation prize(not necessary)
&nbsp;&nbsp;&nbsp;&nbsp;b) Two teams : 40 to 80 % of first prize
&nbsp;&nbsp;&nbsp;&nbsp;c) Three teams : 70 to 100 % of first
&nbsp;&nbsp;&nbsp;&nbsp;d) Four teams : 1st and 2nd<br/>
The final decision regarding the prize money to be awarded will be the discretion of the judges based on the performance of the participants and the quality of the model/robot (in events which involve models/robots)<br/><br/>
6. Decisions of judges will be FINAL and IRREVOCABLE.<br/><br/>
<h2><u>Awarding of prizes</u></h2><br/><br/>
1. Winners of a particular event can collect their prizes as well as certificates from the registration desk 1 hour after the event is over.<br/><br/>
2. For claiming the prize, participants should produce Valid Photo ID as well as CONFIRMED Tathva ID(If applicable) at the registration desk. The prize money should be collected IN PERSON by the winner and under no circumstances will the prize money be awarded through any other means.<br/><br/>
3. If there is any issue regarding the result declaration, it should be resolved with the respective event managers.<br/><br/>
